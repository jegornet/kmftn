    @Test
    fun readsAsPkt2e() {
        val pkt = PktReader.fromByteArray(netmail1Pkt)
        assertIs<Pkt2e>(pkt)
    }

    @Test
    fun headerAddresses() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e

        assertEquals(192.toUShort(), pkt.origZone, "origZone")
        assertEquals(168.toUShort(), pkt.origNet, "origNet")
        assertEquals(1.toUShort(), pkt.origNode, "origNode")
        assertEquals(0.toUShort(), pkt.origPoint, "origPoint")

        assertEquals(192.toUShort(), pkt.destZone, "destZone")
        assertEquals(168.toUShort(), pkt.destNet, "destNet")
        assertEquals(2.toUShort(), pkt.destNode, "destNode")
        assertEquals(0.toUShort(), pkt.destPoint, "destPoint")
    }

    @Test
    fun headerCapWord() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e
        assertEquals(0x0001.toUShort(), pkt.capWord)
    }

    @Test
    fun headerDateTime() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e

        assertEquals(2026.toUShort(), pkt.year, "year")
        assertEquals(3.toUShort(), pkt.month, "month (0-based: 3 = April)")
        assertEquals(13.toUShort(), pkt.day, "day")
        assertEquals(16.toUShort(), pkt.hour, "hour")
        assertEquals(59.toUShort(), pkt.minute, "minute")
        assertEquals(26.toUShort(), pkt.second, "second")
    }

    @Test
    fun headerPassword() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e
        assertContentEquals("abcd1234".encodeToByteArray(), pkt.password)
    }

    @Test
    fun headerProdCode() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e
        assertEquals(0xFF.toUByte(), pkt.prodCodeLo, "prodCodeLo")
        assertEquals(0x10.toUByte(), pkt.prodCodeHi, "prodCodeHi")
    }

    @Test
    fun headerProdVersion() {
        val pkt = PktReader.fromByteArray(netmail1Pkt) as Pkt2e
        assertEquals(1.toUByte(), pkt.prodRevMajor, "prodRevMajor")
        assertEquals(9.toUByte(), pkt.prodRevMinor, "prodRevMinor")
    }

    @Test
    fun containsOneMessage() {
        val pkt = PktReader.fromByteArray(netmail1Pkt)
        assertEquals(1, pkt.messages.size)
    }
